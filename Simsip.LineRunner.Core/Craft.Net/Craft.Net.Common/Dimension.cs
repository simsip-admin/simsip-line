namespace Craft.Net.Common
{
    /// <summary>
    /// Specifies all dimensions a player can exist in.
    /// </summary>
    public enum Dimension
    {
        /// <summary>
        /// The hellish dimension characterized by a red
        /// sky, Zombie Pigmen, Magma Cubes, Blazes, and Ghasts.
        /// </summary>
        Nether = -1,
        /// <summary>
        /// The normal overworld, the default spawn.
        /// </summary>
        Overworld = 0,
        /// <summary>
        /// The location of the game's finale, featuring many
        /// enderman and the Ender Dragon.
        /// </summary>
        TheEnd = 1
    }
}